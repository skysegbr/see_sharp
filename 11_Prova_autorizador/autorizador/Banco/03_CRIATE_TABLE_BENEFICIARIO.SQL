CREATE TABLE TB_BENEFICIARIO (
    COD_BENEFICIARIO  NUMBER(6),
    CPF_BENEFICIARIO  NUMBER(11),
    NOME_BENEFICIARIO VARCHAR2(100),
    SEXO_BENEFICIARIO VARCHAR2(1)
);
/

COMMENT ON COLUMN TB_BENEFICIARIO.COD_BENEFICIARIO IS 'CODIGO IDENTIFICADOR DO PLANO DE SAUDE';
COMMENT ON COLUMN TB_BENEFICIARIO.CPF_BENEFICIARIO IS 'CPF DO BENEFICIARIO';
COMMENT ON COLUMN TB_BENEFICIARIO.NOME_BENEFICIARIO IS 'NOME DO BENEFICIARIO';
COMMENT ON COLUMN TB_BENEFICIARIO.SEXO_BENEFICIARIO IS 'SEXO DO BENEFICIARIO';

ALTER TABLE TB_BENEFICIARIO
    ADD CONSTRAINT PK_BENEFICIARIO PRIMARY KEY (COD_BENEFICIARIO)
    USING INDEX;
/

CREATE SEQUENCE SQ_BENEFICIARIO
