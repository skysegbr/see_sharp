CREATE TABLE TB_FORNECEDOR (
    COD_FORN  NUMBER(6),
    CNPJ_FORN NUMBER(14),
    NOME_FORN VARCHAR2(300),
    COD_EAN   NUMBER(13),
    NOME_PROD VARCHAR2(100),
    TP_PROD   NUMBER(2),
    DT_INI    DATE,
    DT_FIM    DATE
);


COMMENT ON COLUMN TB_FORNECEDOR.COD_FORN IS 'SEQUENCIAL UNICO DO FORNECEDOR';
COMMENT ON COLUMN TB_FORNECEDOR.CNPJ_FORN IS 'CNPJ DO FORNECEDOR';
COMMENT ON COLUMN TB_FORNECEDOR.NOME_FORN IS 'NOME DO FORNECEDOR';
COMMENT ON COLUMN TB_FORNECEDOR.COD_EAN IS 'CODIGO DE BARRAS DO PRODUTO';
COMMENT ON COLUMN TB_FORNECEDOR.NOME_PROD IS 'NOME DO PRODUTO';
COMMENT ON COLUMN TB_FORNECEDOR.TP_PROD IS 'TIPO DO PRODUTO';
COMMENT ON COLUMN TB_FORNECEDOR.DT_INI IS 'DATA INICIAL DO STATUS';
COMMENT ON COLUMN TB_FORNECEDOR.DT_FIM IS 'DATA FU=INAL DO STATUS';

ALTER TABLE TB_FORNECEDOR
    ADD CONSTRAINT PK_TB_FORNECEDOR PRIMARY KEY (COD_FORN, CNPJ_FORN, COD_EAN)
    USING INDEX;


CREATE INDEX INX_FORNECEDOR_DT ON TB_FORNECEDOR (COD_FORN, DT_FIM);


CREATE SEQUENCE SQ_FORNECEDOR
MINVALUE 1
MAXVALUE 999999999999
START WITH 1000
INCREMENT BY 1
NOCACHE;
