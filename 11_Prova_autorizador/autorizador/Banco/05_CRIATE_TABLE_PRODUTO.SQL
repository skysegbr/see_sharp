CREATE TABLE TB_PRODUTO (
    COD_PROD    NUMBER(6),
    TP_PRODUTO  NUMBER(2),
    NOME_PROD   VARCHAR2(100),
    DT_INI      DATE,
    DT_FIM      DATE
);

COMMENT ON COLUMN TB_PRODUTO.COD_PROD IS 'CODIGO PRODUTO NA SAUDE NOVA PBM';
COMMENT ON COLUMN TB_PRODUTO.TP_PRODUTO IS 'TIPO DO PRODUTO';
COMMENT ON COLUMN TB_PRODUTO.NOME_PROD IS 'NOME DO PRODUTO';
COMMENT ON COLUMN TB_PRODUTO.DT_INI IS 'DATA INICIO DA SITUACAO';
COMMENT ON COLUMN TB_PRODUTO.DT_FIM IS 'DATA FIM DA SITUACAO';

ALTER TABLE TB_PRODUTO
    ADD CONSTRAINT PK_COD_PROD PRIMARY KEY (COD_PROD)
    USING INDEX;

CREATE SEQUENCE SQ_PRODUTO
MINVALUE 1
MAXVALUE 9999999999
START WITH 1000
INCREMENT BY 1
NOCACHE;

CREATE INDEX INX_PRODUTO_DT_FIM ON TB_PRODUTO (COD_PROD, DT_FIM);





