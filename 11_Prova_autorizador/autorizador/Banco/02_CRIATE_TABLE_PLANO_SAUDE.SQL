CREATE TABLE TB_PLANO_SAUDE (
    COD_PLAS    NUMBER(6),
    CNPJ_PLAS   NUMBER(14),
    NOME_PLAS   VARCHAR2(50),
    DT_INI      DATE,
    DT_FIM      DATE
);
/

COMMENT ON COLUMN TB_PLANO_SAUDE.COD_PLAS IS 'CODIGO IDENTIFICADOR DO PLANO DE SAUDE';
COMMENT ON COLUMN TB_PLANO_SAUDE.CNPJ_PLAS IS 'IDENTIFICADOR UNICO DO PLANO DE SAUDE';
COMMENT ON COLUMN TB_PLANO_SAUDE.NOME_PLAS IS 'NOME FANTASIA DO PLANO DE SAUDE';
COMMENT ON COLUMN TB_PLANO_SAUDE.DT_INI IS 'DATA INICIO DA SITUACAO';
COMMENT ON COLUMN TB_PLANO_SAUDE.DT_FIM IS 'DATA FIM DA SITUACAO';

ALTER TABLE TB_PLANO_SAUDE
    ADD CONSTRAINT PK_PLANO_SAUDE PRIMARY KEY (COD_PLAS, CNPJ_PLAS)
    USING INDEX;
/

CREATE SEQUENCE SQ_PLANO_SAUDE
MINVALUE 1
MAXVALUE 9999999999
START WITH 1000
INCREMENT BY 1
NOCACHE;
/

CREATE INDEX INX_PLANO_SAUDE_DT_FIM ON TB_PLANO_SAUDE (COD_PLAS, DT_FIM);





